// Helper function to find which group an interest belongs to
export const findGroupForInterest = (interest: string): string | null => {
  for (const [groupName, groupData] of Object.entries(COMMUNITY_GROUPS)) {
    if (groupData.interests.includes(interest)) {
      return groupName;
    }
  }
  return null;
};

// Helper function to get icon component name for a group
export const getIconForGroup = (groupName: string): string => {
  return COMMUNITY_GROUPS[groupName as keyof typeof COMMUNITY_GROUPS]?.icon || "Users";
};